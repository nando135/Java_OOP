    private void editSupplyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSupplyBtnActionPerformed
        String supplierId = (String) supplierIDCmbBox.getSelectedItem();
        String supplierCode = supplierCodeTxtField.getText().trim();
        String supplierName = supplierNameTxtField.getText().trim();
        String contactInfo = contactInfoTxtField.getText().trim();
        String address = addressTxtField.getText().trim();
        String itemId = itemIDTxtField.getText().trim();

        try {
            // Validate supplier ID
            if (supplierId == null || supplierId.isEmpty()) {
                throw new IllegalArgumentException("No Supplier ID selected.");
            }

            // Validate required fields
            if (supplierCode.isEmpty() || supplierName.isEmpty()) {
                throw new IllegalArgumentException("Supplier Code and Supplier Name cannot be empty.");
            }

            // Validate item ID
            if (itemId.isEmpty() || itemService.getItemById(itemId) == null) {
                throw new IllegalArgumentException("Invalid Item ID. Please ensure the item exists.");
            }

            // Validate contact info and address
            if (contactInfo.isEmpty()) {
                throw new IllegalArgumentException("Contact Info cannot be empty.");
            }
            if (address.isEmpty()) {
                throw new IllegalArgumentException("Address cannot be empty.");
            }

            // Update the supplier in the database
            Supplier updatedSupplier = new Supplier(supplierId, supplierCode, supplierName, contactInfo, address, itemId);
            supplierService.updateSupplier(updatedSupplier);

            JOptionPane.showMessageDialog(this, "Supplier updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            // Clear fields
            supplierCodeTxtField.setText("");
            supplierNameTxtField.setText("");
            contactInfoTxtField.setText("");
            addressTxtField.setText("");
            itemIDTxtField.setText("");

            // Close the form or navigate back
            this.dispose();
                } catch (IllegalArgumentException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }

    }//GEN-LAST:event_editSupplyBtnActionPerformed

