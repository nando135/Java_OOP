    private void loadSupplierIDComboBox() {
            List<Supplier> suppliers = supplierService.getAllSuppliers(); // Fetch suppliers
            DefaultComboBoxModel<String> supplierIdModel = new DefaultComboBoxModel<>();

            for (Supplier supplier : suppliers) {
                supplierIdModel.addElement(supplier.getSupplierId()); // Populate supplier IDs
            }

            supplierIDCmbBox.setModel(supplierIdModel); // Set to ComboBox

            // Load details for the first supplier if the list isn't empty
            if (!suppliers.isEmpty()) {
                String firstSupplierId = suppliers.get(0).getSupplierId();
                supplierIDCmbBox.setSelectedItem(firstSupplierId);
                loadSupplierDetails(firstSupplierId); // Load details
            }

            // Add listener to handle ComboBox changes
            supplierIDCmbBox.addActionListener(evt -> {
                String selectedSupplierId = (String) supplierIDCmbBox.getSelectedItem();
                if (selectedSupplierId != null) {
                    loadSupplierDetails(selectedSupplierId);
                }
            });
        }



        
        
private void loadSupplierDetails(String supplierId) {
    // Retrieve the supplier as Optional
    Optional<Supplier> supplierOptional = supplierService.getSupplierById(supplierId);

    if (supplierOptional.isPresent()) {
        // Extract the Supplier object
        Supplier supplier = supplierOptional.get();

        // Set fields, ensuring null checks
        supplierCodeTxtField.setText(supplier.getSupplierCode() != null ? supplier.getSupplierCode() : "");
        supplierNameTxtField.setText(supplier.getSupplierName() != null ? supplier.getSupplierName() : "");
        contactInfoTxtField.setText(supplier.getContactInfo() != null ? supplier.getContactInfo() : "");
        addressTxtField.setText(supplier.getAddress() != null ? supplier.getAddress() : "");
        itemIDTxtField.setText(supplier.getItemId() != null ? supplier.getItemId() : "");
    } else {
        // Clear fields if supplier is not found
        supplierCodeTxtField.setText("");
        supplierNameTxtField.setText("");
        contactInfoTxtField.setText("");
        addressTxtField.setText("");
        itemIDTxtField.setText("");

        JOptionPane.showMessageDialog(this, "Supplier not found!", "Error", JOptionPane.ERROR_MESSAGE);
    }
}




        

