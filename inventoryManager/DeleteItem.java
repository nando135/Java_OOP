    private void loadItemIDComboBox() {
        List<Item> items = itemService.getAllItems();
        DefaultComboBoxModel<String> itemIdModel = new DefaultComboBoxModel<>();

        // Populate the combo box with item IDs
        for (Item item : items) {
            itemIdModel.addElement(item.getItemId());
        }
        itemIDCmbBox.setModel(itemIdModel);

        // Add listener to display item details when an item ID is selected
        itemIDCmbBox.addActionListener(evt -> {
            String selectedItemId = (String) itemIDCmbBox.getSelectedItem();
            if (selectedItemId != null) {
                loadItemDetails(selectedItemId);
            }
        });

        // Load details for the first item by default
        if (!items.isEmpty()) {
            loadItemDetails(items.get(0).getItemId());
        }
    }
        private void loadItemDetails(String itemId) {
        // Retrieve the item using ItemService
        Item item = itemService.getItemById(itemId);

        if (item != null) {
            // Set item details to corresponding labels
            usernameField.setText(item.getItemCode()); // Item Code
            fullNameField.setText(item.getItemName()); // Item Name
            emailField.setText(item.getSupplierId());  // Supplier ID
            roleField.setText(String.valueOf(item.getReorderLevel())); // Reorder Level
            jLabel8.setText(String.valueOf(item.getPrice()));          // Price
            jLabel10.setText(String.valueOf(item.getStockLevel()));    // Stock Level
        } else {
            // Clear fields if the item is not found
            usernameField.setText("");
            fullNameField.setText("");
            emailField.setText("");
            roleField.setText("");
            jLabel8.setText("");
            jLabel10.setText("");

            JOptionPane.showMessageDialog(this, "Error: Item not found!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


