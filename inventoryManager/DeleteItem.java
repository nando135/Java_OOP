    private void DeleteItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteItemBtnActionPerformed
        String itemId = (String) itemIDCmbBox.getSelectedItem();

        // Ensure an item is selected
        if (itemId == null || itemId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select an item to delete.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this item?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Delete the item using ItemService
                itemService.deleteItem(itemId);
                
                // logging inventory updates
                InventoryUpdateService inventoryUpdateService = new InventoryUpdateService();
                InventoryUpdates update = inventoryUpdateService.createDeleteUpdate(itemId);
                inventoryUpdateService.writeInventoryUpdate(update);

                JOptionPane.showMessageDialog(this, "Item deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Clear displayed fields and reload ComboBox
                usernameField.setText("");
                fullNameField.setText("");
                emailField.setText("");
                roleField.setText("");
                jLabel8.setText("");
                jLabel10.setText("");
                loadItemIDComboBox();
            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        this.dispose();


    }//GEN-LAST:event_DeleteItemBtnActionPerformed

