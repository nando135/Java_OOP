    private void loadSupplierCmbBox() {
        supplierIdCmbBox.removeAllItems();
        List<Supplier> suppliers = supplierService.getAllSuppliers();
        for (Supplier supplier : suppliers) {
            // Format as "SupplierID SupplierName"
            String displayText = supplier.getSupplierId() + " (" + supplier.getSupplierName() + ")";
            supplierIdCmbBox.addItem(displayText);
        }
    }
    
    private void loadPurchaseOrderCmbBox() {
        purchaseOrderIdCmbBox.removeAllItems();
        PurchaseOrderService purchaseOrderService = new PurchaseOrderService();
        List<PurchaseOrder> purchaseOrders = purchaseOrderService.getAllPurchaseOrders();
        
        for (PurchaseOrder po : purchaseOrders) {
            purchaseOrderIdCmbBox.addItem(String.valueOf(po.getPurchaseOrderID()));
        }
    }
    
    
