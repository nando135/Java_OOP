    private void AddSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSupplierBtnActionPerformed
        try {
            // get selected values from UI
            String selectedSupplierId = (String) supplierIdCmbBox.getSelectedItem();
            String selectedPurchaseOrderId = (String) purchaseOrderIdCmbBox.getSelectedItem();
            String amountText = supplierNameTxtField.getText().trim();
            String selectedStatus = (String) itemIdCmbBox.getSelectedItem();
            java.util.Date selectedDate = paymentDatePicker.getDate();
            
            // validation
            if (selectedSupplierId == null || selectedPurchaseOrderId == null || amountText.isEmpty() || selectedStatus == null || selectedDate == null ) {
                throw new IllegalArgumentException("All fields are required.");
            }
            
            if (!amountText.matches("\\d+(\\.\\d+)?")) {
                throw new IllegalArgumentException("Invalid amount format. Please use numbers only.");
            }
            
            // validation for commas in all text fields
            if (amountText.contains(",")) {
                throw new IllegalArgumentException("Fields cannot contain commas (,).");
            }

            double amount = Double.parseDouble(amountText);
            
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String formattedPaymentDate = dateFormat.format(selectedDate);
            
            // convert supplierId and purchaseOrderId to Integers
            String supplierId = (String) supplierIdCmbBox.getSelectedItem();
            int purchaseOrderId = Integer.parseInt(selectedPurchaseOrderId.split(" ")[0]);
            // payment constructor
            Payment newPayment = new Payment(0, purchaseOrderId, supplierId, amount, selectedStatus, formattedPaymentDate );
            // Add Payment
            paymentService.addPayment(newPayment);
            
            // display success and clear fields
            JOptionPane.showMessageDialog(this, "Payment added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            
            new AdminMainPage().setVisible(true);
            this.dispose();
            
            
        }
        catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "An unexpected error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_AddSupplierBtnActionPerformed
    
