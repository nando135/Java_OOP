    private void DeletePaymentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletePaymentBtnActionPerformed
       
        String selectedPaymentId = (String) paymentIdCmbBox.getSelectedItem();
        
        if (selectedPaymentId == null) {
            JOptionPane.showMessageDialog(this, "Please select a payment to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this payment?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                int paymentId = Integer.parseInt(selectedPaymentId.split(" ")[0]);
                paymentService.deletePayment(paymentId);
                JOptionPane.showMessageDialog(this, "Payment deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadComboBox();
                new AdminMainPage().setVisible(true);
                this.dispose();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid payment ID format.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_DeletePaymentBtnActionPerformed

