    private void paymentIdCmbBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paymentIdCmbBoxActionPerformed
        String selectedPaymentId = (String) paymentIdCmbBox.getSelectedItem();
        if (selectedPaymentId != null) {
            try {
                int paymentId = Integer.parseInt(selectedPaymentId.split(" ")[0]);
                Payment payment = paymentService.getPaymentById(paymentId);
                if (payment != null) {
                    fillPaymentDetails(payment);
                } else {
                    clearFields();
                }
                
            } catch (NumberFormatException e) {
                clearFields();
            }
        }
    }//GEN-LAST:event_paymentIdCmbBoxActionPerformed

    
    private void loadComboBox() {
        List<Payment> payments = paymentService.getPaymentHistory();
        DefaultComboBoxModel<String> paymentIdModel = new DefaultComboBoxModel<>();

        for (Payment payment : payments) {
            paymentIdModel.addElement(String.valueOf(payment.getPaymentID()));
        }

        paymentIdCmbBox.setModel(paymentIdModel);

        if (!payments.isEmpty()) {
            paymentIdCmbBox.setSelectedIndex(0);
            fillPaymentDetails(payments.get(0));
        } else {
            clearFields();
        }
    }

    private void fillPaymentDetails(Payment payment) {
        purchaseOrderField.setText(String.valueOf(payment.getPurchaseOrderID()));
        supplierIdField.setText(String.valueOf(payment.getSupplierID()));
        paymentAmountField.setText(String.valueOf(payment.getAmount()));
        paymentStatusField.setText(payment.getStatus());

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date date = dateFormat.parse(payment.getPaymentDate());
            paymentDatePicker.setDate(date);
        } catch (Exception e) {
            paymentDatePicker.setDate(null);
        }
        paymentIdCmbBox.setSelectedItem(String.valueOf(payment.getPaymentID()) + " (Payment)");
    }


    private void clearFields() {
        purchaseOrderField.setText("Choose Payment ID");
        supplierIdField.setText("Choose Payment ID");
        paymentAmountField.setText("Choose Payment ID");
        paymentStatusField.setText("Choose Payment ID");
        paymentDatePicker.setDate(null);
        paymentIdCmbBox.setSelectedIndex(-1);
    }
   

