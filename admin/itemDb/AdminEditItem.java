    private void loadItemCmbBox() {
        ItemService itemService = new ItemService();
        SupplierService supplierService = new SupplierService();

        List<Item> items = itemService.getAllItems();
        List<Supplier> suppliers = supplierService.getAllSuppliers();
        DefaultComboBoxModel<String> itemIdModel = new DefaultComboBoxModel<>();

        // populate item ID combo box
        for (Item item : items) {
            itemIdModel.addElement(item.getItemId());
        }
        supplierIDCmbBox1.setModel(itemIdModel);

        // setup supplier combo box
        supplierIDCmbBox.removeAllItems();
        for (Supplier supplier : suppliers) {
            String displayText = String.format("%s (%s)", supplier.getSupplierName(), supplier.getSupplierId());
            supplierIDCmbBox.addItem(displayText);
        }

        // fill in fields when item ID is selected
        supplierIDCmbBox1.addActionListener(evt -> {
            String selectedItemId = (String) supplierIDCmbBox1.getSelectedItem();

            for (Item item : items) {
                if (item.getItemId().equals(selectedItemId)) {
                    // populate all fields
                    itemCodeTxtField.setText(item.getItemCode());
                    itemNameTxtField.setText(item.getItemName());
                    priceTxtField.setText(String.valueOf(item.getPrice()));
                    reorderLevelTxtField.setText(String.valueOf(item.getReorderLevel()));
                    StockLevelTxtField.setText(String.valueOf(item.getStockLevel()));

                    // select the correct supplier in combo box
                    String itemSupplierId = item.getSupplierId();
                    for (int i = 0; i < supplierIDCmbBox.getItemCount(); i++) {
                        String supplierEntry = supplierIDCmbBox.getItemAt(i);
                        if (supplierEntry.endsWith("(" + itemSupplierId + ")")) {
                            supplierIDCmbBox.setSelectedIndex(i);
                            break;
                        }
                    }
                    break;
                }
            }
        });

    }


