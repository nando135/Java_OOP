    private void EditItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditItemBtnActionPerformed
        String itemId = (String) supplierIDCmbBox1.getSelectedItem();
        String itemCode = itemCodeTxtField.getText().trim();
        String itemName = itemNameTxtField.getText().trim();
        String selectedSupplier = (String) supplierIDCmbBox.getSelectedItem();
        String supplierId = "";

        // validation for commas in all text fields
        if (itemCode.contains(",") || itemName.contains(",") || 
            priceTxtField.getText().contains(",") || 
            reorderLevelTxtField.getText().contains(",") || 
            StockLevelTxtField.getText().contains(",")) {
            JOptionPane.showMessageDialog(this, "Fields cannot contain commas (,).", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        
        // extract supplier ID from the selection from "SupplierName (SupplierId)" format
        if (selectedSupplier != null && selectedSupplier.contains("(") && selectedSupplier.contains(")")) {
            supplierId = selectedSupplier.substring(selectedSupplier.lastIndexOf("(") + 1, selectedSupplier.lastIndexOf(")"));
        }

        ItemService itemService = new ItemService();
        InventoryUpdateService inventoryUpdateService = new InventoryUpdateService();
        List<Item> items = itemService.getAllItems();

        try {
            // get original item data before update
            Item originalItem = items.stream()
                .filter(i -> i.getItemId().equals(itemId))
                .findFirst()
                .orElseThrow(() -> new IllegalArgumentException("Item not found"));

            // Parse numeric values
            double price = Double.parseDouble(priceTxtField.getText().trim());
            int reorderLevel = Integer.parseInt(reorderLevelTxtField.getText().trim());
            int stockLevel = Integer.parseInt(StockLevelTxtField.getText().trim());

            // create updated item object
            Item updatedItem = new Item(
                itemId,
                itemCode,
                itemName,
                supplierId,
                price,
                reorderLevel,
                stockLevel
            );

            // update item in database
            String managerId = LoginSession.getCurrentUserSession();
            itemService.updateItem(updatedItem);

            // log the edit details
            int changeInStock = stockLevel - originalItem.getStockLevel(); // calculate stock change
            InventoryUpdates editUpdate = inventoryUpdateService.createEditUpdate(itemId, changeInStock);
            inventoryUpdateService.writeInventoryUpdate(editUpdate);

            JOptionPane.showMessageDialog(this, "Item updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

            // clear fields
            itemCodeTxtField.setText("");
            itemNameTxtField.setText("");
            priceTxtField.setText("");
            reorderLevelTxtField.setText("");
            StockLevelTxtField.setText("");
            supplierIDCmbBox.setSelectedIndex(-1);
            supplierIDCmbBox1.setSelectedIndex(-1);

            // return to admin page
            new AdminMainPage().setVisible(true);
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for price, reorder level, and stock level", 
                "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_EditItemBtnActionPerformed

