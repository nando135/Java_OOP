    private void loadSupplierCmbBox() {
        SupplierService supplierService = new SupplierService();
        List<Supplier> suppliers = supplierService.getAllSuppliers();

        supplierIDCmbBox.removeAllItems();
        for (Supplier supplier : suppliers) {
            // display format "Supplier Name (SUP001)"
            String displayText = supplier.getSupplierName() + " (" + supplier.getSupplierId() + ")";
            supplierIDCmbBox.addItem(displayText);
        }
    }
