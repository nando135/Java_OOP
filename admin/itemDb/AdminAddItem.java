    private void AddItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddItemBtnActionPerformed
        ItemService itemService = new ItemService();
        InventoryUpdateService inventoryUpdateService = new InventoryUpdateService();

        try {
            // get values from form fields
            String itemCode = itemCodeTxtField.getText().trim();
            String itemName = itemNameTxtField.getText().trim();
            String selectedSupplier = (String) supplierIDCmbBox.getSelectedItem();
            if (selectedSupplier == null || selectedSupplier.isEmpty()) {
                throw new IllegalArgumentException("Please select a supplier");
            }

            // extract supplier ID from the format "SupplierName (SupplierId)"
            String supplierId = selectedSupplier.substring(
                selectedSupplier.lastIndexOf("(") + 1,
                selectedSupplier.lastIndexOf(")")
            );

            // validation for commas in all text fields
            if (itemCode.contains(",") || itemName.contains(",") || 
                priceTxtField.getText().contains(",") || 
                reorderLevelTxtField.getText().contains(",") || 
                StockLevelTxtField.getText().contains(",")) {
                throw new IllegalArgumentException("Fields cannot contain commas (,).");
            }

            // parse numeric values
            double price;
            int reorderLevel;
            int stockLevel;
            try {
                price = Double.parseDouble(priceTxtField.getText().trim());
                reorderLevel = Integer.parseInt(reorderLevelTxtField.getText().trim());
                stockLevel = Integer.parseInt(StockLevelTxtField.getText().trim());
            } catch (NumberFormatException e) {
                throw new IllegalArgumentException("Please enter valid numbers for price, reorder level, and stock level");
            }

            // generate item ID
            String itemId = itemService.generateNextItemId();

            // add new item
            Item newItem = new Item(
                itemId,
                itemCode,
                itemName,
                supplierId,
                price,
                reorderLevel,
                stockLevel
            );

            // add item to database
            String managerId = LoginSession.getCurrentUserSession();
            itemService.addItem(newItem);

            // log the add details using 
            InventoryUpdates update = inventoryUpdateService.createAddUpdate(itemId, stockLevel);
            inventoryUpdateService.writeInventoryUpdate(update);

            JOptionPane.showMessageDialog(this, "Item added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

            // clear input fields
            itemCodeTxtField.setText("");
            itemNameTxtField.setText("");
            priceTxtField.setText("");
            reorderLevelTxtField.setText("");
            StockLevelTxtField.setText("");
            supplierIDCmbBox.setSelectedIndex(-1);

            // return to admin page
            new AdminMainPage().setVisible(true);
            this.dispose();

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_AddItemBtnActionPerformed

