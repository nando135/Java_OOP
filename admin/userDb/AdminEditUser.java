    private void loadUserIDComboBox() {
        UserService userManagementService = new UserService();
        List<User> users = userManagementService.loadData();
        DefaultComboBoxModel<String> userIdModel = new DefaultComboBoxModel<>();

        // populate userID combo box
        for (User user : users) {
            userIdModel.addElement(user.getUserID());
        }

        userIDCmbBox.setModel(userIdModel);

        // fill in the fields when userID is selected
        userIDCmbBox.addActionListener(evt -> {
            String selectedUserId = (String) userIDCmbBox.getSelectedItem();

            for (User user : users) {
                if (user.getUserID().equals(selectedUserId)) {
                    usernameTxtField.setText(user.getUsername());
                    nameTxtField.setText(user.getName());
                    emailTxtField.setText(user.getEmail());
                    passwordTxtField.setText(user.getPassword());

                    // match role in combo box
                    String userRole = user.getRole();
                    for (int i = 0; i < roleCmbBox.getItemCount(); i++) {
                        if (((String) roleCmbBox.getItemAt(i)).contains("(" + userRole + ")")) {
                            roleCmbBox.setSelectedIndex(i);
                            break;
                        }
                    }
                    break;
                }
            }
        });
    }

