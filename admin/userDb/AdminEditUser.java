    private void EditUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditUserBtnActionPerformed
        String userID = (String) userIDCmbBox.getSelectedItem();
        String username = usernameTxtField.getText().trim();
        String name = nameTxtField.getText().trim();
        String email = emailTxtField.getText().trim();
        String password = passwordTxtField.getText().trim();
        String selectedRole = (String) roleCmbBox.getSelectedItem();
        String role = "";

        // filter role according to text inside of ()
        if (selectedRole != null && selectedRole.contains("(") && selectedRole.contains(")")) {
            role = selectedRole.substring(selectedRole.indexOf('(') + 1, selectedRole.indexOf(')')).trim();
        }

        UserService userManagementService = new UserService();
        List<User> users = userManagementService.loadData();

        try {
            // get original user data before update
            User originalUser = users.stream()
                .filter(u -> u.getUserID().equals(userID))
                .findFirst()
                .orElseThrow(() -> new IllegalArgumentException("User not found"));
                
            // create updated user object
            User updatedUser = new User(userID, name, role, username, password, email);
            
            // validation
            UserValidationService.validateUserInput(userID, userID, name, role, username, password, email, users);
            userManagementService.updateUser(updatedUser);

            // log admin edit changes
            UserLogService logService = new UserLogService();
            String adminUsername = LoginSession.getCurrentUserSession();
            String editedFields = logService.getEditDetails(originalUser, updatedUser);
            logService.writeUserActionLog(
                username, 
                adminUsername,
                UserLogService.UserAction.USER_EDITED,
                editedFields
            );

            JOptionPane.showMessageDialog(this, "User updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);

            // clear fields
            usernameTxtField.setText("");
            nameTxtField.setText("");
            emailTxtField.setText("");
            passwordTxtField.setText("");
            roleCmbBox.setSelectedIndex(-1);
            
            new AdminMainPage().setVisible(true);
            this.dispose();
            
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_EditUserBtnActionPerformed

