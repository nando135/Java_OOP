    private void loadSupplierCmbBox() {
        itemIdCmbBox.removeAllItems();
        ItemService itemService = new ItemService(); // create an instance of item service
        List<Item> items = itemService.getAllItems(); // get all items from the service

        for (Item item : items) {
            // format as "ItemID ItemName"
            String displayText = item.getItemId() + " (" + item.getItemName() +")";
            itemIdCmbBox.addItem(displayText);
        }
    }

    
    private void clearFields() {
        supplierNameTxtField.setText("");
        supplierCodeTxtField.setText("");
        supplierAddressTxtField.setText("");
        contactInfoTxtField.setText("");
    }

