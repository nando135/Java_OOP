    private void AddSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSupplierBtnActionPerformed
        try {
            String supplierName = supplierNameTxtField.getText().trim();
            String supplierCode = supplierCodeTxtField.getText().trim();
            String supplierAddress = supplierAddressTxtField.getText().trim();
            String contactInfo = contactInfoTxtField.getText().trim();
            String selectedItemId = (String) itemIdCmbBox.getSelectedItem();

            // validate inputs
            if (supplierName.isEmpty() || supplierCode.isEmpty() || supplierAddress.isEmpty() || contactInfo.isEmpty() || selectedItemId == null) {
                throw new IllegalArgumentException("All fields are required.");
            }

            // validate for commas
            if (supplierName.contains(",") || supplierCode.contains(",") || supplierAddress.contains(",") || contactInfo.contains(",")) {
                throw new IllegalArgumentException("Fields cannot contain commas (,).");
            }

            // generate supplier ID and create supplier
            String supplierId = supplierService.generateNextSupplierId();
            Supplier newSupplier = new Supplier(supplierId, supplierCode, supplierName, contactInfo, supplierAddress, selectedItemId);

            // add supplier to service
            supplierService.addSupplier(newSupplier);

            // display success and clear fields
            JOptionPane.showMessageDialog(this, "Supplier added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            clearFields();

            new AdminMainPage().setVisible(true);
            this.dispose();

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validation Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_AddSupplierBtnActionPerformed

