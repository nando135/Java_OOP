    private void DeleteSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteSupplierBtnActionPerformed
        String selectedSupplierId = (String) supplierIdCmbBox.getSelectedItem();

        if (selectedSupplierId == null) {
            JOptionPane.showMessageDialog(this, "Please select a supplier to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this supplier?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            supplierService.deleteSupplier(selectedSupplierId);
            new AdminMainPage().setVisible(true);;
            this.dispose();
            JOptionPane.showMessageDialog(this, "Supplier deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadComboBox();
        }
    }//GEN-LAST:event_DeleteSupplierBtnActionPerformed

