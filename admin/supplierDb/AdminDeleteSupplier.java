    private void supplierIdCmbBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supplierIdCmbBoxActionPerformed
        String selectedSupplierId = (String) supplierIdCmbBox.getSelectedItem();

        if (selectedSupplierId != null) {
            Supplier supplier = supplierService.getSupplierById(selectedSupplierId).orElse(null);
            if (supplier != null) {
                fillSupplierDetails(supplier);
            } else {
                clearFields();
            }
        }
    }//GEN-LAST:event_supplierIdCmbBoxActionPerformed

    private void supplierCodeCmbBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supplierCodeCmbBoxActionPerformed
        String selectedSupplierCode = (String) supplierCodeCmbBox.getSelectedItem();

        if (selectedSupplierCode != null) {
            List<Supplier> suppliers = supplierService.getAllSuppliers();
            for (Supplier supplier : suppliers) {
                if (supplier.getSupplierCode().equals(selectedSupplierCode)) {
                    fillSupplierDetails(supplier);
                    break;
                }
            }
        }
    }//GEN-LAST:event_supplierCodeCmbBoxActionPerformed

    private void loadComboBox() {
        List<Supplier> suppliers = supplierService.getAllSuppliers();

        DefaultComboBoxModel<String> idModel = new DefaultComboBoxModel<>();
        DefaultComboBoxModel<String> codeModel = new DefaultComboBoxModel<>();

        for (Supplier supplier : suppliers) {
            idModel.addElement(supplier.getSupplierId()); 
            codeModel.addElement(supplier.getSupplierCode());
        }

        supplierIdCmbBox.setModel(idModel);
        supplierCodeCmbBox.setModel(codeModel);

        if (!suppliers.isEmpty()) {
            supplierIdCmbBox.setSelectedIndex(0);
            supplierCodeCmbBox.setSelectedIndex(0);
            fillSupplierDetails(suppliers.get(0));
        } else {
            clearFields();
        }
    }
    
    private void fillSupplierDetails(Supplier supplier) {
        supplierNameField.setText(supplier.getSupplierName());
        supplierContactField.setText(supplier.getContactInfo());
        supplierAddressField.setText(supplier.getAddress());
        supplierItemIdField.setText(supplier.getItemId());
        supplierCodeCmbBox.setSelectedItem(supplier.getSupplierCode()); // sync code
        supplierIdCmbBox.setSelectedItem(supplier.getSupplierId()); // sync ID
    }

    private void clearFields() {
        supplierNameField.setText("Choose Item");
        supplierContactField.setText("Choose Item");
        supplierAddressField.setText("Choose Item");
        supplierItemIdField.setText("Choose Item");
        supplierIdCmbBox.setSelectedIndex(-1);
        supplierCodeCmbBox.setSelectedIndex(-1);
    }



