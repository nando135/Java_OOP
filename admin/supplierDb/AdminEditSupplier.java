    private void EditSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditSupplierBtnActionPerformed
        String supplierId = (String) supplierIdCmbBox.getSelectedItem();
        String supplierCode = supplierCodeTxtField.getText().trim();
        String supplierName = supplierNameTxtField.getText().trim();
        String contactInfo = supplierContactInfoTxtField.getText().trim();
        String supplierAddress = supplierAddressTxtField.getText().trim();
        String selectedItemId = (String) supplierItemIdCmbBox.getSelectedItem();

        // validation for commas in all text fields
        if (supplierCode.contains(",") || supplierName.contains(",") || contactInfo.contains(",") || supplierAddress.contains(",")) {
           JOptionPane.showMessageDialog(this, "Fields cannot contain commas (,).", "Validation Error", JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        if (supplierId == null || supplierId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a supplier ID.", 
                    "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // retrieve the original supplier
        Supplier originalSupplier = supplierService.getAllSuppliers().stream()
                .filter(s -> s.getSupplierId().equals(supplierId))
                .findFirst()
                .orElse(null);

        if (originalSupplier == null) {
            JOptionPane.showMessageDialog(this, "Selected supplier not found.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // create updated supplier object
        Supplier updatedSupplier = new Supplier(
                supplierId,
                supplierCode,
                supplierName,
                contactInfo,
                supplierAddress,
                selectedItemId
        );

        // update supplier in database
        boolean updated = supplierService.updateSupplier(updatedSupplier);
        if (updated) {
            JOptionPane.showMessageDialog(this, "Supplier updated successfully.", 
                    "Success", JOptionPane.INFORMATION_MESSAGE);

            // clear fields
            supplierCodeTxtField.setText("");
            supplierNameTxtField.setText("");
            supplierContactInfoTxtField.setText("");
            supplierAddressTxtField.setText("");
            supplierItemIdCmbBox.setSelectedIndex(-1);
            supplierIdCmbBox.setSelectedIndex(-1);

            new AdminMainPage().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update supplier.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditSupplierBtnActionPerformed

