    private void loadComboBoxes() {
        List<Supplier> suppliers = supplierService.getAllSuppliers();
        List<Item> items = itemService.getAllItems();

        // Populate supplier IDs
        DefaultComboBoxModel<String> supplierIdModel = new DefaultComboBoxModel<>();
        for (Supplier supplier : suppliers) {
            supplierIdModel.addElement(supplier.getSupplierId());
        }
        supplierIdCmbBox.setModel(supplierIdModel);

        // Populate item IDs for associated items
        DefaultComboBoxModel<String> itemIdModel = new DefaultComboBoxModel<>();
        for (Item item : items) {
            itemIdModel.addElement(item.getItemId());
        }
        supplierItemIdCmbBox.setModel(itemIdModel);

        supplierIdCmbBox.setSelectedIndex(-1);
        supplierItemIdCmbBox.setSelectedIndex(-1);

        // Add a listener to populate fields when a supplier is selected
        supplierIdCmbBox.addActionListener(evt -> {
            String selectedSupplierId = (String) supplierIdCmbBox.getSelectedItem();
            if (selectedSupplierId != null) {
                Supplier selectedSupplier = suppliers.stream()
                        .filter(s -> s.getSupplierId().equals(selectedSupplierId))
                        .findFirst()
                        .orElse(null);

                if (selectedSupplier != null) {
                    supplierCodeTxtField.setText(selectedSupplier.getSupplierCode());
                    supplierNameTxtField.setText(selectedSupplier.getSupplierName());
                    supplierContactInfoTxtField.setText(selectedSupplier.getContactInfo());
                    supplierAddressTxtField.setText(selectedSupplier.getAddress());

                    // Debugging lines start here
                    String assocItemId = selectedSupplier.getItemId();
                    System.out.println("Associated Item ID from supplier: " + assocItemId);

                    for (int i = 0; i < supplierItemIdCmbBox.getItemCount(); i++) {
                        System.out.println("Item at " + i + ": " + supplierItemIdCmbBox.getItemAt(i));
                    }
                    // Debugging lines end here

                    // Now match assocItemId with the combo box items
                    if (assocItemId != null) {
                        assocItemId = assocItemId.trim();
                        for (int i = 0; i < supplierItemIdCmbBox.getItemCount(); i++) {
                            String comboItem = supplierItemIdCmbBox.getItemAt(i).trim();
                            if (comboItem.equals(assocItemId)) {
                                supplierItemIdCmbBox.setSelectedIndex(i);
                                break;
                            }
                        }
                    } else {
                        supplierItemIdCmbBox.setSelectedIndex(-1);
                    }
                } else {
                    // Clear fields if no supplier found
                    supplierCodeTxtField.setText("");
                    supplierNameTxtField.setText("");
                    supplierContactInfoTxtField.setText("");
                    supplierAddressTxtField.setText("");
                    supplierItemIdCmbBox.setSelectedIndex(-1);
                }
            }
        });

    }


