private void loadAnnuallyReportsToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows
        List<salesreport> reports = annuallyService.loadData();

        for (salesreport report : reports) {
            model.addRow(new Object[]{
                report.getEntryId(),
                report.getItemId(),
                report.getSalesDateMonth(),
                report.getSalesDateYear(),
                report.getQuantitySold(),
                report.getTotalSales()/report.getQuantitySold()
            });
        }
    }



  
      
    private void calculateTotalSalesForSelectedItem(int year) {
        List<salesreport> reports = annuallyService.loadData();
        double totalSales = 0.0;

        for (salesreport report : reports) {
            if (report.getSalesDateYear() == year) {
                totalSales += report.getTotalSales()/ report.getQuantitySold();
            }
        }
   JOptionPane.showMessageDialog(this,
            "Total Sales in "  + year + ": RM " + String.format("%.2f", totalSales),
            "Sales Information",
            JOptionPane.INFORMATION_MESSAGE);
    }

